<h4><strong>Kelompok Kerja (POKJA) I</strong></h4>
<a href="/users/tahunpokjai" class="btn btn-outline-primary">Data kegiatan Pokja I</a>
<p>Mempunyai tanggungjawab dalam mengelola program penghayatan dan pengamalan pancasila dan program gotong royong.</p>
<h5>Tugas</h5>
<ol>
    <li>Memantapkan kerukunan dan toleransi antar umat beragama, saling menghormati dan menghargai dalam wadah Negara Kesatuan Republik Indonesia.</li>
    <li>Meningkatkan ketahanan keluarga dalam rangka mewujudkan kesadaran setiap warga tentang&nbsp; Penghayatan dan Pengamalan Pancasila melalui Pembinaan Kesadaran Bela Negara (PKBN)</li>
    <li>Memantapan Pola Asuh Anak dan remaja dalam keluarga serta perlindungan anak melalui Lokakarya dan Ujicoba.</li>
    <li>Peningkatan pemahaman dan pengamalan perilaku budi pekerti dan sopan santun dalam keluarga dan lingkungan</li>
    <li>Meningkatkan pemahaman peraturan perundangan yang berkait dengan pencegahan Kekerasan Dalam Rumah Tangga (KDRT), pencegahan perdagangan orang (trafficking), peningkatan pemahaman penyalahgunaan narkoba melalui life skill dan parenting skill.</li>
    <li>Meningkatkan kesadaran hidup bergotong royong, kesetiakawanan sosial, keamanan lingkungan, Tentara Manunggal Membangun Desa (TMMD) dan lain lainnya.</li>
    <li>Memberdayakan LANSIA dalam kegiatan yang produktif dan menjadi teladan&nbsp; dalam keluarga dan lingkungannya.</li>
</ol>
<h5>Prioritas Program</h5>
<ul>
    <li><strong>Penghayatan dan Pengamalan Pancasila</strong></li>
</ul>
<p>Menumbuhkan ketahanan keluarga melalui kesadaran bermasyarakat, berbangsa dan bernegara perlu dilaksanakan pemahaman secara terpadu :</p>
<p>a).&nbsp;&nbsp;&nbsp; Pembinaan Kesadaran Bela Negara (PKBN):</p>
<p>PKBN mencakup 5 (lima) unsur:</p>
<blockquote>
    <blockquote>
        <ol>
            <li>Kecintaan tanah air,</li>
            <li>Kesadaran berbangsa dan bernegara,</li>
            <li>Keyakinan atas kebenaran Pancasila,</li>
            <li>Kerelaan berkorban untuk Bangsa dan Negara serta</li>
            <li>Memiliki kemampuan awal bela Negara.</li>
        </ol>
    </blockquote>
</blockquote>
<p>b).&nbsp;&nbsp;&nbsp; Kesadaran Hukum (KADARKUM):.</p>
<p>KADARKUM adalah upaya untuk meningkatkan pemahaman tentang peraturan perundang-undangan diprioritaskan di PKK untuk pencegahan PKDRT, Trafficking, Perlindungan Anak, NARKOBA Dan lain-lain.</p>
<p>c).&nbsp;&nbsp;&nbsp; Pola Asuh Anak dan Remaja</p>
<p>Pola Asuh anak dan remaja adalah upaya untuk menumbuhkan dan membangun perilaku, budi pekerti, sopan santun didalam keluarga sesuai budaya bangsa.</p>
<p>d).&nbsp;&nbsp;&nbsp; Pemahaman dan Ketrampilan Hidup (Life Skill And Parenting Skill)</p>
<p>Pemahaman dan ketrampilan hidup adalah upaya menumbuhkan kesadaran orang tua dalam upaya pencegahan penyalahgunaan Narkoba.</p>
<p>e).&nbsp;&nbsp;&nbsp; Pemahaman tertib administrasi dalam rangka meningkatkan dan mewujudkan tertib administrasi kependudukan di keluarga.</p>
<ul>
    <li><strong>Gotong Royong</strong></li>
</ul>
<p>Kegiatan gotong royong dilaksanakan dengan membangun kerjasama yang baik antar sesama: keluarga, warga dan kelompok untuk mewujudkan semangat persatuan dan kesatuan.</p>
<blockquote>
    <ol>
        <li>Menumbuhkan kesadaran, kesetiakawanan sosial, bertenggang rasa dan kebersamaan serta saling menghormati antar umat beragama</li>
        <li>Memberdayakan LANSIA agar dapat menjaga kesehatan fisik dan mental, kebugaran, keterampilan agar dapat melaksanakan kegiatan secara produktif dan menjadi teladan bagi keluarga dan lingkungannya.</li>
        <li>Berpartisipasi dalam pelaksanaan kegiatan bakti sosial, kegiatan Tentara Manunggal Membangun Desa (TMMD).</li>
    </ol>
</blockquote>
